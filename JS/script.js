let price = prompt("Введите стоимость покупки: ");
if (price > 500 && price <= 1000) {
    alert("Стоимось покупки без скидки: " + price + " руб." + "\n\n" + "Скидка 3%!" + "\n" + "Сумма к оплате: " + price * 0.97 + " руб.");
} else if (price > 1000) {
    alert("Стоимось покупки без скидки: " + price + " руб." + "\n\n" + "Скидка 5%!" + "\n" + "Сумма к оплате: " + price * 0.95 + " руб.");
} else {
    alert("Скидок нет!" + "\n\n" + "Сумма к оплате: " + price + " руб.");
}
